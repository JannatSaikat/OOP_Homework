package conceptMobile;

public class EncapsulationIdentity {
	
	private String Name ;
	private int IMEI;
	
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getIMEI() {
		return IMEI;
	}
	public void setIMEI(int iMEI) {
		IMEI = iMEI;
	}
	
	

}
